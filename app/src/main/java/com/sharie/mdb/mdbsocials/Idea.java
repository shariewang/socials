public class Idea{

    public String title, author, description, key, email, date;
    public int stars;

    public Idea(){}

    public Idea(String name, String author, String email, String describe, String key, String date, int stars){
        this.title = name;
        this.author = author;
        this.description = describe;
        this.key = key;
        this.email = email;
        this.stars = stars;
        this.date = date;
    }









}
